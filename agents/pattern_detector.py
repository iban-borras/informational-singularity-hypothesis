    def get_pattern_summary(self) -> Dict[str, Any]:
        """Return a summary of detected patterns."""
        if not self.detected_patterns:
            return {"total_patterns": 0}

        total_patterns = len(self.detected_patterns)
        avg_length = np.mean([len(p['pattern_data']) for p in self.detected_patterns])
        avg_recurrence = np.mean([p['recurrence'] for p in self.detected_patterns])
        avg_density = np.mean([p.get('density', 0) for p in self.detected_patterns])

        methods_used = set(p.get('method', 'unknown') for p in self.detected_patterns)

        return {
            "total_patterns": total_patterns,
            "average_length": avg_length,
            "average_recurrence": avg_recurrence,
            "average_density": avg_density,
            "methods_used": list(methods_used),
            "top_patterns": sorted(self.detected_patterns,
                                 key=lambda p: p['recurrence'],
                                 reverse=True)[:5]
        }


